int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return ((unsigned)*s1 - (unsigned)*s2);
		s1++;
		s2++;
	}
	return ((unsigned)*s1 - (unsigned)*s2);
}
