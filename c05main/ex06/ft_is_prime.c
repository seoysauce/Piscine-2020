int	ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb == 0 || nb == 1)
		return (0);
	else if (nb == 2)
		return (1);
	else if (nb > 2)
		while (nb % i != 0 && i < nb)
			i++;
	if (i == nb)
		return (1);
	return (0);
}
